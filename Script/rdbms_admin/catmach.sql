declare 
  CURSOR refschs is
    select distinct owner from dba_dependencies
      where referenced_owner = 'PUBLIC' and referenced_name = 'UTL_FILE' and owner != 'SYS';
begin
  for c1 in refschs loop
    EXECUTE IMMEDIATE 'grant execute on UTL_FILE to '||c1.owner;
  end loop;
end;
